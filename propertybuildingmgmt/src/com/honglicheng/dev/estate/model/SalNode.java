package com.honglicheng.dev.estate.model;

public class SalNode {
	
	//房间属性名
	private String salName;
	
	//相同房间属性户数总面积
	private Float salCount;

	public String getSalName() {
		return salName;
	}

	public void setSalName(String salName) {
		this.salName = salName;
	}

	public Float getSalCount() {
		return salCount;
	}

	public void setSalCount(Float salCount) {
		this.salCount = salCount;
	}
	
}
