package com.honglicheng.dev.estate.model;

public class NopNode {

	//人数规模名
	private String nopName;
	
	//人数规模户数
	private Integer nopCount;

	public String getNopName() {
		return nopName;
	}

	public void setNopName(String nopName) {
		this.nopName = nopName;
	}

	public Integer getNopCount() {
		return nopCount;
	}

	public void setNopCount(Integer nopCount) {
		this.nopCount = nopCount;
	} 
	
}
